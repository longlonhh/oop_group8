    private void resetFieldsIfConditionsMet() {
        if (!selectedTable.isEmpty() && startTime != null && endTime != null) {
            txt1.setText("");
            txt2.setText("");
        }
    }
    private void btnbatdauActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbatdauActionPerformed
        // TODO add your handling code here:
        if (selectedTable.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn bàn trước khi bắt đầu.");
            return;
        }
        startTime = getStartTimeFromDatabase(selectedTable);
        if (startTime == null) {
            startTime = LocalTime.now().withNano(0);
            saveStartTimeToDatabase(selectedTable, startTime);
        }
        txt1.setText(startTime.toString());
    }//GEN-LAST:event_btnbatdauActionPerformed

    private void btnketthucActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnketthucActionPerformed
        // TODO add your handling code here:
        if (startTime == null) {
            JOptionPane.showMessageDialog(this, "Chưa có giờ bắt đầu cho bàn đã chọn.");
            return;
        }
        endTime = LocalTime.now().withNano(0);
        txt2.setText(endTime.toString());
        saveEndTimeToDatabase(selectedTable, endTime);
    }//GEN-LAST:event_btnketthucActionPerformed

    private void btntinhgioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntinhgioActionPerformed
        // TODO add your handling code here:
        if (startTime == null || endTime == null) {
            JOptionPane.showMessageDialog(this, "Vui lòng bắt đầu và kết thúc trước khi tính giờ.");
            return;
        }
        Duration duration = Duration.between(startTime, endTime);
        long totalSeconds = duration.getSeconds();
        long hours = totalSeconds / 3600;
        long minutes = (totalSeconds % 3600) / 60;
        long seconds = totalSeconds % 60;

        double totalAmount = (totalSeconds / 3600.0) * 50000;

        lbsogiochoi.setText(String.format("Số giờ chơi: %02d:%02d:%02d", hours, minutes, seconds));
        lbtongtien.setText(String.format("Tổng Tiền: %.2f VND", totalAmount));
        saveTotalAmountToDatabase(selectedTable, (long) totalAmount);
    }//GEN-LAST:event_btntinhgioActionPerformed

    private void saveStartTimeToDatabase(String tableName, LocalTime startTime) {
        String sql = "INSERT INTO " + tableName + " (ngay, gio_bat_dau) VALUES (?, ?)";
        try (Connection conn = Class_ket_noi.getConnection(); PreparedStatement ps = conn.prepareStatement(sql)) {
            ps.setString(1, lbngay.getText().replace("Ngày: ", "").split(" ")[0]);
            ps.setString(2, startTime.toString());
            ps.executeUpdate();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

    private LocalTime getStartTimeFromDatabase(String tableName) {
        String sql = "SELECT gio_bat_dau FROM " + tableName + " WHERE id = (SELECT MAX(id) FROM " + tableName + ") AND gio_ket_thuc IS NULL";
        try (Connection conn = Class_ket_noi.getConnection(); PreparedStatement ps = conn.prepareStatement(sql)) {
            ResultSet rs = ps.executeQuery();
            if (rs.next()) {
                return LocalTime.parse(rs.getString("gio_bat_dau"));
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
        return null;
    }

    private void saveEndTimeToDatabase(String tableName, LocalTime endTime) {
        String sql = "UPDATE " + tableName + " SET gio_ket_thuc = ? WHERE id = (SELECT MAX(id) FROM " + tableName + ")";
        try (Connection conn = Class_ket_noi.getConnection(); PreparedStatement ps = conn.prepareStatement(sql)) {
            ps.setString(1, endTime.toString());
            ps.executeUpdate();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

    private void saveTotalAmountToDatabase(String tableName, long totalAmount) {
        String sql = "UPDATE " + tableName + " SET tong_tien = ? WHERE id = (SELECT MAX(id) FROM " + tableName + ")";
        try (Connection conn = Class_ket_noi.getConnection(); PreparedStatement ps = conn.prepareStatement(sql)) {
            ps.setLong(1, totalAmount);
            ps.executeUpdate();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

